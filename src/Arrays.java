public class Arrays {
    public static void main(String[] args) {
        String [] cars = {"Volvo", "BMW", "Ford", "Mazda"}; //arrays
        for(int num=0;num<4;num=num+1){   //for문으로 배열 나열하기
               System.out.println(cars[num]);  //배열은 0부터 시작이니깐 num=0 
        }
        //return 0;
    }
}

